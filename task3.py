# Основна функція для побудови малюнку
def print_pattern(N):
    # Цикл по рядках від N до 1
    for i in range(N, 0, -1):
        # Виведення чисел у рядку
        for j in range(N, i - 1, -1):
            print(j, end=" ")
        print()  # Переход на новий рядок після кожного рядка чисел

# Основна частина програми
def main():
    # Введення числа N з перевіркою на правильний діапазон
    while True:
        try:
            N = int(input("Введіть число N (1 < N < 9): "))
            if 1 < N < 9:
                break
            else:
                print("Число має бути між 1 та 9. Спробуйте ще раз.")
        except ValueError:
            print("Неправильне введення. Введіть ціле число.")

    # Виведення малюнку
    print_pattern(N)

# Запуск програми
main()
